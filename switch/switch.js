let buah = "Apel";
//Switch Case
switch (buah) {
  case "Mangga":
    console.log("Ini buah Mangga");
    break;
  case "Apel":
    console.log("Ini buah Apel");
    break;
  case "Jeruk":
    console.log("Ini buah Jeruk");
    break;
  default:
    console.log("Ini buah yang tidak ada di list");
}
